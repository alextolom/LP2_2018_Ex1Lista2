public class Processamento {

    private double valor;
    private double resposta;

    public double getValor() {
        return valor;
    }

    public void setValor(double valor) {
        this.valor = valor;
    }

    public double getResposta() {
        resposta = valor*2;
        return resposta;
    }

   
}
